#include "ft_42sh.h"

void		ft_cmdestroy(t_cmd **begin)
{
	t_cmd	*tmp;
	t_cmd	*list;

	list = *begin;
	tmp = NULL;
	while (list)
	{
		if (list->next)
			tmp = list->next;
		else
			tmp = NULL;
		free(list);
		list = tmp;
	}
}
